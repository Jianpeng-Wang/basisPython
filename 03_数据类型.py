#!/usr/bin/env pytho      
# -*- coding: utf-8 -*-

# int -- 整型
a = 1
print(type(a))

# float -- 浮点型，就是小数
b = 1.1
print(type(b))

# str -- 字符串，数据特点：必须用引号包住
c = 'hello world'
print(type(c))

# bool -- 布尔类型
d = True
print(type(d))

# list -- 列表
e = [1, 1, 2, 5]
print(type(e))

# tuple -- 元组
f = (1, 2, 3)
print(type(f))

# set -- 集合
g = {1, 2, 2}
print(type(g))

# dict -- 字典 -- 键值对
h = {'name': 'tom', 'age': 12}
print(type(h))

